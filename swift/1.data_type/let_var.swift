// Swift 中，语句末尾的分号是可选的，通常我们不写。
let name = "John"
var age = 20

// 变量声明的正确语法是 `var variableName: Type = value`
// 这与 C++/Java/C# 的 `Type variableName = value` 不同。
var num: Int = 20
var str: String = "John"
var isStudent: Bool = true
var height: Float = 1.75
var weight: Double = 70.5

// 打印所有变量的值
print(name)
print(age)
print(num)
print(str)
print(isStudent)
print(height)
print(weight)